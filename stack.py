"""
Модуль с реализацией стека

Стек используется для сохранения состояний данных после каждого этапа
конвейера и возможности отката к предыдущему состоянию
"""

class Stack:
    """Стек на основе списка Python для хранения состояний конвейера"""
    def __init__(self):
        """Инициализация пустого стека"""
        self._items = []

    def push(self, item):
        """Добавление элемента на вершину стека"""
        self._items.append(item)

    def pop(self):
        """Удаление и возврат элемента с вершины стека"""
        if self.is_empty():
            return None
        return self._items.pop()

    def is_empty(self):
        """Проверка, пуст ли стек"""
        return len(self._items) == 0

    def __len__(self):
        """Возвращает количество элементов в стеке"""
        return len(self._items)